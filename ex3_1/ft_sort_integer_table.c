void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int j;
	int tmp;

	i = size - 1;
	j = 0;
	while (i >= 0)
	{
		while (j < i)
		{
			if (tab[j] > tab[j + 1])
			{
				tmp = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = tmp;
			}
			j++;
		}
		j = 0;
		i--;
	}
}
