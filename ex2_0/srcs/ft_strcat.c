char	*ft_strcat(char *dest, char *src)
{
	int i;
	int a;

	i = -1;
	a = 0;
	while (dest[a] != '\0')
		a++;
	while (src[++i] != '\0')
		dest[a + i] = src[i];
	dest[a + i] = '\0';
	return (dest);
}
